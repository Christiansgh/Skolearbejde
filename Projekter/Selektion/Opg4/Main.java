package Opg4;

public class Main {
    public static void main(String[] args) {
        int first = 1;
        int second = 2;
        int third = 3;
        int largest = 0;
        if(first > largest) {
            largest = first;
        }
        if(second > largest) {
            largest = second;
        }
        if(third > largest) {
            largest = third;
        }
        System.out.println("The largest number is " + largest);
    }
}
